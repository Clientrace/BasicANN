# Define the Node or Neuron
#------------------------

class Node(object):

	def __init__(self, data, next):
		self.data = data
		self.next = next

	def getData(self):
		return self.data

	def getNext(self):
		return self.next

	def addNext(self,next):
		self.next = nex

